package model;

public class Color {
	// Mã màu vàng dành cho những cảnh báo
	public static String yellow = "\u001B[33;1m";
	// Màu vàng nhạt dành cho những thông báo
	public static String lightYellow = "\u001B[93m";
	// Mã màu đỏ, tượng trưng cho quân đỏ
	public static String red = "\u001B[31m";
	// Mã màu xanh tượng trưng cho quân xanh
	public static String blue = "\u001B[34m";
	// Mã để reset màu
	public static String reset = "\u001B[0m";
}
